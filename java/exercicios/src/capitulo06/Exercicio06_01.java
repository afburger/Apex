package capitulo06;

/**
 * 1 - Escreva um programa que escreva os números entre 1 e 1000.
 * 
 * @author André Felipe Bürger (andre@iburger.com.br)
 *
 */
public class Exercicio06_01 {

	public static void main(String[] args) {
		
		int numero = 2;
		while (numero < 1000) {
			System.out.println(numero);
			numero++;
		}
		
	}
}
