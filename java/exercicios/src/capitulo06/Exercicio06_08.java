package capitulo06;

/**
 * 8 - Escreva um programa que mostre todos os números entre 1 e 100;
 * 
 * @author André Felipe Bürger (andre@iburger.com.br)
 *
 */
public class Exercicio06_08 {

	public static void main(String[] args) {
		
		for (int i = 1; i < 100; i++) {
			if (i == 1) {
				i++;
			}
			System.out.println(i);
		}


		
	}
}
